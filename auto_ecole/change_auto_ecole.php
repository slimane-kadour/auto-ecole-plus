        <br />
<p> Vous ne vous sentez pas bien dans votre auto-école actuelle ? Vous avez raté votre permis et vous n'arrivez pas à obtenir une nouvelle date de passage ? Vous déménagez? 
<p> Autant de bonnes raisons pour avoir envie de changer d'air. Mais attention un tel changement n'est pas anodin.</p><br />

<h3>Un changement lourd de conséquences <a name="consequence" id="consequence"></a></h3>
<p> Vous pouvez  quitter librement une auto-école pour poursuivre votre formation dans une autre.</p>
<p>Mais dans les faits, vous aurez beaucoup de difficultés à obtenir une place d’examen dans une autre auto-école surtout si vous avez déjà passé l'épreuve de conduite une première fois. D'autant plus que vous serez perçu comme un client "à problème".</p>
<p> De plus avec ce changement vous serez obligé de payer des frais dans la nouvelle auto école et il se peut que le contrat initial prévoit des indemnités de rupture. </p>
<p>Vous avez donc intérêt à <a href="choix-auto-ecole-1.php">choisir tout de suite la bonne école de conduite</a>, pour éviter des frais de transfert, la perte des heures non effectuées dans votre forfait, une recherche difficile et une attente pour une place à l'examen du permis de conduire. <br />
Ainsi l'appartenance de votre auto école à un réseau (CER, ECF…) facilitera un changement d'auto école. En cas de déménagement, votre dossier pourra être transféré dans une autre école membre du réseau. Sinon, en cas de différend, une solution amiable pourra vous être proposée par une commission d'arbitrage.
</p><br />

<h3>Lisez attentivement votre contrat <a name="contrat" id="contrat"></a></h3>
<p> Si vous avez signé un contrat, dont vous pouvez obtenir une copie auprès du directeur de l’école de conduite, regardez attentivement les clauses prévues en cas de rupture. </p>
<p>Dans tous les cas, les leçons qui auraient été payées à l’avance, et non dispensées, peuvent être remboursées. <br />
  En revanche, tout forfait type forfait code  sera perdu en cas de transfert !
  Nous vous conseillons donc de consommer ce que vous avez payé ! </p>
<br />

<h3>Comment vous y prendre? <a name="process" id="process"></a></h3>
<p> <strong><img src="http://www.ladocumentationfrancaise.fr/catalogue/9782110064691/9782110064691.jpg" alt="livret-d'apprentissage-permis" width="85" height="125" hspace="20" vspace="10" align="right" />1) Récupérez votre dossier</strong><br />
  Pour changer d’auto-école, vous devrez récupérer le dossier d’inscription CERFA ou “Dossier 02″ auprès de l'ancienne auto-école. Ce dossier a une validité de 6 ans (N° NEPH prolongeable). </p>

<p> C'est un document administratif qui appartient à l'élève du fait de ses frais d'inscription. Sa restitution ne peut être l'objet d'un paiement, sauf frais de désarchivage qui doivent être spécifiés sur le contrat. </p>
<p> En cas de faillite de l’auto-école, vous devrez demander un duplicata à la préfecture au service “permis de conduire” qui transmetra votre demande au délégué à la formation du conducteur.</p>
<p>
Vous pouvez aussi récupérer le livret d’apprentissage qui détaille le parcours et la formation suivie par l'élève. Ce document aidera votre nouveau formateur à adapter vos cours de conduite à vos points forts et points faibles et à estimer l'avancement de votre formation ! </p>
<p><strong><img src="images/voitureautoecole1.JPG" alt="changer-auto-ecole" width="149" height="112" hspace="20" vspace="10" align="right" />2) Passez une évaluation avec une autre auto-école</strong><br />
  Avant de signer tout nouveau contrat, la nouvelle auto école doit obligatoirement procéder à une évaluation.<br />
A l'issue, en résulte un nombres d'heures "proposées". Libre à l'élève d'accepter ou pas.</p>
<p>
<em>Important</em> : Pour maximiser vos chances de convaincre une seconde auto-école, défendez au mieux votre dossier. Soyez prêt à faire des concessions, telles qu' un engagement sur un nombre minimal de leçons de conduite supérieur à celui imaginé, pour montrer que vous ne serez pas une source de difficultés.<br /> 
</p>
<br />

	<h3>Vos recours en cas de litige <a name="recours" id="recours"></a></h3>
<p> Il est préférable d'utiliser une solution amiable. Faute de dialogue, envoyez une lettre de mise en demeure avec AR avant d'en venir à des  solutions plus radicales.</p>
<p>Si la situation reste bloquée, contactez le délégué à la formation du conducteur de votre département, une association de consommateurs ou la Direction Générale de la Concurrence, de la Consommation et de la Répression des Fraudes (DGCCRF). In fine, adressez-vous au  tribunal d'instance.    </p>
