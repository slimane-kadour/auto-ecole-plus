          <h3>Comment changer d'auto-école </h3>
		  <p><img width="380" height="70" src="images/doublecommandetop5.gif" align="right" /></p>
		<div id="menu_sous_titre"><strong>
		<a href="#consequence">1.&nbsp;&nbsp;&nbsp;Les conséquences</a><br />  
