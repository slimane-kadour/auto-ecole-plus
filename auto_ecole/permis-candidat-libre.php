	 <p>
		<h3>Comment s'inscrire </h3>
	<p>Pour passer le permis de conduire, il est possible de ne pas faire appel à une auto-école. <br />
	Le candidat s'adresse à la préfecture, au service du permis de conduire, pour s'inscrire à l'examen,  en remplissant le <a href="http://www2.equipement.gouv.fr/formulaires/fic_pdf/11246.pdf">formulaire cerfa n°11246*02 </a>et en demandant de passer le permis en candidat libre.  <br />
	
	Il recevra une convocation à l'épreuve du code et à l'épreuve pratique par la préfecture.   <br />
    Le grand avantage de cette formule est l'économie des frais de dossier ou des frais de passage des épreuves du permis facturées par l'auto école.
    <br />
    <em>Attention</em> : avant de vous inscrire vérifiez les délais d'inscription auprès de la préfecture. Si vous êtes pressés privilégiez une formule accélérée en auto école.
	  </p> 
      <h3> Un bon plan en cas de retrait de permis ou de permis étranger </h3>
      <p> Le passage du permis en candidat libre est adapté aux candidats ayant une expérience de la conduite. En effet dans ce cas, ils peuvent passer les épreuves pratiques et théoriques sans passer par une auto école. <br />
      Les candidats ayant subi un retrait de permis ou les possesseurs d'un permis étranger hors Union Européenne présents depuis plus d'un an sur le territoire devraient donc considérer cette option.
        </p>
	  <h3>Attention avant de passer uniquement le "code", en candidat libre !</h3>
  <p> Peu d'auto écoles accepteront de vous proposer un forfait conduite intéressant. Elles vous factureront les heures à l'unité ou un forfait 20 heures au prix du forfait total. En effet, la marge de l'auto école est plus importante sur les heures de théorie avec un moniteur pour une dizaine d'élèves que pour les heures de conduite. <br />
C'est dommage alors que de nombreux supports (livres ou DVD) ou sites en ligne existent pour se former efficacement seul à son domicile pas cher!
	</p>
	
		<h3>Obligations du candidat</h3>
		<p>Pour le passage de l'épreuve de la conduite, le candidat devra se conformer aux obligations suivantes : 
		<ul >
		<li>Prouver un <strong>minimum de 20 heures de formation</strong> pendant l'apprentissage. <br /> 
		  Les heures prises suivant les différents modes d'apprentissage s'additionnent :</li>
		  <ul>
		    <li> le livret d'apprentissage prouve les heures de formation en auto école</li>
		    <li> les factures de <a href="conseils-location-double-commande.php">location de voiture à double commande</a> recensant les heures de location.</li>
	      </ul>        <br />
        <li><strong>Le jour de l'examen</strong>, le candidat doit : </li>
          <ul>
            <li>être accompagné d'une personne titulaire du permis de conduire correspondant à la catégorie du véhicule présenté,    </li>
            <li>souscrire une assurance spéciale pour conduire le jour de l'examen,</li>
            <li>utiliser un véhicule spécialement équipé, comportant notamment une double commande de frein et d'embrayage, 2 rétroviseurs intérieurs et 2 		rétroviseurs latéraux réglés pour le conducteur et l'accompagnateur. </li>
          </ul>
        
		</ul>
         </p>
        <p>Pour le jour de l'examen, la  solution la plus économique est alors de <a href="liste-societe-location-voiture-double-commande.php">louer un véhicule à double commande</a> en s'assurant de la disponibilité le jour de l'examen et de la fourniture par la société de location de voiture auto école  de l'attestation d'assurance nécessaire.
		  </p>
		<p>
<h3>Les difficultés à l'inscription en candidat libre </h3>
Les places d'examen sont accessibles en priorité pour les candidats présentés par une auto-école et le bureau de la circulation tentera de vous décourager car la gestion des candidats libre entraîne une surcharge d'activité pour ce service de la préfecture.<br />

En revanche, une fois l'inscription prise en compte vous recevrez directement par courrier vos dates de convocation et vous pourrez commencez à vous organiser</p>



<br />
<p>
<h3>Changement d'auto école</h3>
<p>En cas de <a href="changer-auto-ecole.php">changement d'auto école</a>, il est difficile de transférer votre dossier dans une autre auto école.</p>
<p>En effet, au delà de la méfiance inspirée par un comportement peu fréquent, le nombre de places attribué pour l’épreuve pratique est lié au nombre de candidats examinés au cours des 12 derniers mois en première présentation. En acceptant de vous présenter le gérant défavorise donc son auto-école. <br />
  <br />
  En revanche, les auto écoles accueillent en règle générale avec bienveillance les candidats libres pour leur donner des le&ccedil;ons de conduite sans avoir à leur affecter une place à l'examen. </p>
  <p>
En cas de changement d'auto-école, la formule du permis en candidat libre sera ainsi particulièrement adaptée.</p>
